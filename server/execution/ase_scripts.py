# 7. DOS / PDOS
# ─────────────────────────────────────────────────────────────────────────────
def script_static(
    kpoints: str = "8 8 1",
    encut: int = 400,
    system_desc: str = "",
    **_kw,
) -> str:
    """Step 1: Self-consistent SCF that writes WAVECAR+CHGCAR for follow-up calcs."""
    kpts = [int(x) for x in kpoints.replace("x"," ").split()[:3]] if kpoints else [8,8,1]
    lines = [
        _header(f"Static SCF — writes WAVECAR+CHGCAR for DOS/band/ELF follow-up — {system_desc or 'surface'}"),
        "from ase.io import read",
        "from ase.calculators.vasp import Vasp",
        "",
        "# Use the RELAXED geometry from a previous geo-opt run",
        "atoms = read('CONTCAR')",
        "",
        "# Step 1 of 2: SCF run — LWAVE+LCHARG=True so DOS/band/ELF can read them",
        "calc = Vasp(",
        "    istart=0, icharg=2,   # fresh SCF",
        "    ibrion=-1, nsw=0,",
        f"    encut={encut},",
        f"    kpts={kpts},",
        "    xc='PBE', gamma=True,",
        "    ismear=1, sigma=0.2,   # Methfessel-Paxton for metals",
        "    ediff=1e-6,",
        "    lwave=True,   # MUST be True — needed by ELF, band, COHP",
        "    lcharg=True,  # MUST be True — needed by DOS, band",
        "    ncore=4, algo='Fast',",
        ")",
        "atoms.set_calculator(calc)",
        "atoms.get_potential_energy()",
        "print('SCF done — WAVECAR and CHGCAR written.')",
        "print('Next: run ase-dos.py / ase-band.py / ase-elf.py in this directory.')",
    ]
    return "\n".join(lines)


def script_dos(
    kpoints: str = "12 12 1",
    encut: int = 400,
    pdos_species: str = "",
    **_kw,
) -> str:
    """Step 2: Non-SCF DOS/PDOS (ICHARG=11). Requires prior SCF run with LCHARG=True."""
    kpts = [int(x) for x in kpoints.replace("x"," ").split()[:3]] if kpoints else [12,12,1]
    lines = [
        _header("DOS / PDOS — non-SCF (Step 2 of 2). Requires CHGCAR from SCF run."),
        "from ase.io import read",
        "from ase.calculators.vasp import Vasp",
        "import numpy as np",
        "",
        "# PREREQUISITE: run ase-static.py first (same directory, same POSCAR/CONTCAR)",
        "atoms = read('CONTCAR')",
        "",
        "# Step 2 of 2: non-SCF with dense k-mesh and tetrahedron smearing",
        "calc = Vasp(",
        "    istart=1, icharg=11,  # non-SCF: reads CHGCAR, does NOT update charge",
        "    ibrion=-1, nsw=0,",
        f"    encut={encut},",
        f"    kpts={kpts},         # denser k-mesh for well-converged DOS",
        "    xc='PBE', gamma=False,",
        "    ismear=-5,            # tetrahedron — no smearing artifacts in DOS",
        "    sigma=0.05,",
        "    lorbit=11,            # lm-decomposed PDOS (s,p,d per atom)",
        "    nedos=2000,",
        "    emin=-15.0, emax=10.0,",
        "    lwave=False, lcharg=False,",
        "    ncore=4,",
        ")",
        "atoms.set_calculator(calc)",
        "atoms.get_potential_energy()",
        "",
        "# Save total DOS",
        "try:",
        "    from ase.dft.dos import DOS as _DOS",
        "    dos_obj = _DOS(calc, width=0.0)",
        "    d = dos_obj.get_dos()",
        "    e = dos_obj.get_energies()",
        "    np.savetxt('dos_total.dat', np.column_stack([e, d]),",
        "               header='energy_eV  DOS_states_per_eV')",
        "    print('Total DOS saved to dos_total.dat')",
        "except Exception as ex:",
        "    print(f'DOS parse warning: {ex}')",
        "",
        "# d-band center from DOSCAR (parse atom-projected d-DOS)",
        "try:",
        "    from ase.calculators.vasp import VaspDos",
        "    dos_vasp = VaspDos('DOSCAR')",
        "    ef = calc.get_fermi_level()",
        "    # Example: d-band of first atom (index 0)",
        "    d_dos = dos_vasp.site_dos(0, 'd')  # integrated over m_l",
        "    e_grid = dos_vasp.energy",
        "    mask = e_grid < ef",
        "    if mask.sum() > 0:",
        "        d_center = np.trapz(e_grid[mask]*d_dos[mask], e_grid[mask]) / np.trapz(d_dos[mask], e_grid[mask])",
        "        print(f'd-band center (atom 0): {d_center:.3f} eV (vs E_F={ef:.3f} eV)')",
        "except Exception as ex:",
        "    print(f'd-band center parse warning: {ex}')",
    ]
    return "\n".join(lines)


# ─────────────────────────────────────────────────────────────────────────────
# 8. Band structure
# ─────────────────────────────────────────────────────────────────────────────
def script_band(
    encut: int = 400,
    nkpoints: int = 50,
    **_kw,
) -> str:
    lines = [
        _header("Band structure calculation"),
        "from ase.io import read",
        "from ase.calculators.vasp import Vasp",
        "from ase.dft.kpoints import get_special_points, bandpath",
        "",
        "atoms = read('CONTCAR')",
        "lat = atoms.cell.get_bravais_lattice()",
        f"path = lat.bandpath(npoints={nkpoints})",
        "",
        "calc = Vasp(",
        "    istart=1, icharg=11,",
        "    ibrion=-1, nsw=0,",
        f"    encut={encut},",
        "    kpts=path,",
        "    xc='PBE',",
        "    lorbit=11,",
        "    ncore=4,",
        ")",
        "atoms.set_calculator(calc)",
        "atoms.get_potential_energy()",
        "",
        "# Extract and save band energies",
        "try:",
        "    bs = calc.band_structure()",
        "    bs.write('band_structure.json')",
        "    print('Band structure saved to band_structure.json')",
        "except Exception as ex:",
        "    print(f'Band parse error: {ex}')",
    ]
    return "\n".join(lines)


# ─────────────────────────────────────────────────────────────────────────────
# 9. ELF (electron localization function)
# ─────────────────────────────────────────────────────────────────────────────
def script_elf(encut: int = 400, kpoints: str = "8 8 1", **_kw) -> str:
    """ELF — NCORE=1 is a hard VASP requirement when LELF=True."""
    kpts = [int(x) for x in kpoints.replace("x"," ").split()[:3]] if kpoints else [8,8,1]
    lines = [
        _header("ELF — Electron Localization Function. NCORE=1 required (VASP hard requirement)."),
        "from ase.io import read",
        "from ase.calculators.vasp import Vasp",
        "",
        "# PREREQUISITE: run ase-static.py first to generate WAVECAR",
        "atoms = read('CONTCAR')",
        "",
        "# IMPORTANT: NCORE must be 1 — VASP aborts with NCORE>1 when LELF=True",
        "calc = Vasp(",
        "    istart=1, icharg=0,   # read WAVECAR, do SCF (NOT ICHARG=11 — ELF needs SCF)",
        "    ibrion=-1, nsw=0,",
        f"    encut={encut},",
        f"    kpts={kpts},",
        "    xc='PBE', gamma=True,",
        "    prec='Accurate',",
        "    ediff=1e-6,",
        "    ismear=1, sigma=0.2,",
        "    lelf=True,     # write ELFCAR",
        "    lwave=True, lcharg=True,",
        "    ncore=1,       # *** MANDATORY: VASP aborts if NCORE>1 with LELF=True ***",
        ")",
        "atoms.set_calculator(calc)",
        "atoms.get_potential_energy()",
        "print('ELFCAR written.')",
        "print('Visualize in VESTA: isosurface at ELF=0.75 shows lone pairs and bonds.')",
        "print('ELF=1.0: fully localized (lone pairs)  ELF=0.5: electron gas  ELF=0: depleted')",
    ]
    return "\n".join(lines)


# ─────────────────────────────────────────────────────────────────────────────
# 10. Bader charge analysis
# ─────────────────────────────────────────────────────────────────────────────
def script_bader(
    encut: int = 520,
    kpoints: str = "8 8 1",
    **_kw,
) -> str:
    """Bader charge analysis: LAECHG=True → AECCAR0/AECCAR2 → bader binary."""
    kpts = [int(x) for x in kpoints.replace("x"," ").split()[:3]] if kpoints else [8,8,1]
    lines = [
        _header("Bader charge analysis — LAECHG=True, then chgsum.pl + bader binary"),
        "from ase.io import read",
        "from ase.calculators.vasp import Vasp",
        "import subprocess, os",
        "",
        "atoms = read('CONTCAR')",
        "",
        "# Higher ENCUT (520 eV) recommended for accurate all-electron charge density",
        "calc = Vasp(",
        "    istart=0, icharg=2,",
        "    ibrion=-1, nsw=0,",
        f"    encut={encut},",
        f"    kpts={kpts},",
        "    xc='PBE', gamma=True,",
        "    prec='Accurate',",
        "    ediff=1e-6,",
        "    ismear=1, sigma=0.2,",
        "    lreal=False,   # reciprocal projection — required for Bader",
        "    laechg=True,   # write AECCAR0 (core) + AECCAR2 (valence)",
        "    lwave=False, lcharg=True,",
        "    ncore=4,",
        ")",
        "atoms.set_calculator(calc)",
        "atoms.get_potential_energy()",
        "print('VASP done — AECCAR0, AECCAR2, CHGCAR written.')",
        "",
        "# ── Post-processing: Bader charge partitioning ──────────────────────────",
        "# Requires: chgsum.pl (VTST scripts) and bader binary (Henkelman group)",
        "# Download: theory.cm.utexas.edu/bader/",
        "if os.path.exists('AECCAR0') and os.path.exists('AECCAR2'):",
        "    print('Running chgsum.pl AECCAR0 AECCAR2 ...')",
        "    subprocess.run(['chgsum.pl', 'AECCAR0', 'AECCAR2'], check=True)",
        "    print('Running bader CHGCAR -ref CHGCAR_sum ...')",
        "    subprocess.run(['bader', 'CHGCAR', '-ref', 'CHGCAR_sum'], check=True)",
        "    # Parse ACF.dat",
        "    if os.path.exists('ACF.dat'):",
        "        print('\\nBader charges (ACF.dat):')",
        "        print(open('ACF.dat').read()[:2000])",
        "        # Bader charge = Z_ion - q_bader (charge transferred to/from atom)",
        "        import numpy as np",
        "        z_ions = atoms.get_atomic_numbers()",
        "        lines_acf = [l for l in open('ACF.dat') if l.strip() and not l.startswith('-')]",
        "        bader_charges = []",
        "        for line in lines_acf[2:2+len(atoms)]:",  # skip header
        "            cols = line.split()",
        "            if len(cols) >= 5:",
        "                bader_charges.append(float(cols[4]))",
        "        if bader_charges:",
        "            for i, (z, q) in enumerate(zip(z_ions, bader_charges)):",
        "                xfer = z - q",
        "                sym = atoms[i].symbol",
        "                print(f'  Atom {i:3d} ({sym:3s}): Bader charge = {q:.3f} e   charge transfer = {xfer:+.3f} e')",
        "else:",
        "    print('AECCAR files not found — check that LAECHG=True in INCAR.')",
    ]
    return "\n".join(lines)


