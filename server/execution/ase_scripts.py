def generate_script(calc_type: str, system: Dict[str, Any] = None, params: Dict[str, Any] = None) -> str:
    """
    Generate an ASE calculation script.
    calc_type: one of 'surface','molecule','geo','freq','neb','gcdft',
               'static','dos','pdos','band','elf','bader','cdd','work_function','cohp'
    system: dict with element, facet, surface_type, etc.
    params: dict with calculation-specific parameters
    """
    key = (calc_type or "geo").lower().strip()
    fn = _GENERATORS.get(key, script_geo)
    merged = {}
    if system: merged.update(system)
    if params: merged.update(params)
    return fn(**merged)


def script_filename(calc_type: str) -> str:
    return _FILENAMES.get((calc_type or "geo").lower(), f"ase-{calc_type}.py")
