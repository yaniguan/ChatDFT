# 15. HTP batch job script
# ─────────────────────────────────────────────────────────────────────────────
def script_htp_batch(
    db_path: str = "htp_dataset.db",
    encut: int = 450,
    kpoints: str = "4 4 1",
    batch_size: int = 50,
    scheduler: str = "sge",
    **_kw,
) -> str:
    """
    Script that reads pending structures from HTP database, runs single-point
    VASP for each, saves results back to db, exports final extXYZ training set.
    """
    kpts = [int(x) for x in kpoints.replace("x", " ").split()[:3]] if kpoints else [4, 4, 1]
    submit_block_sge = (
        "#!/bin/bash\n"
        "#$ -N htp_batch\n"
        "#$ -cwd\n"
        "#$ -l h_rt=48:00:00\n"
        "#$ -pe shared 16\n"
        "#$ -l h_data=4G\n"
        "module load vasp\n"
        "python htp_batch.py\n"
    ) if scheduler == "sge" else (
        "#!/bin/bash\n"
        "#SBATCH -J htp_batch\n"
        "#SBATCH -N 1\n"
        "#SBATCH --ntasks=16\n"
        "#SBATCH -t 48:00:00\n"
        "module load vasp\n"
        "python htp_batch.py\n"
    )
    lines = [
        _header(f"HTP batch job: single-point VASP for NNP training dataset"),
        "# ── Cluster submission script ─────────────────────────────────────────────",
        f"# Scheduler: {scheduler}",
        f"# Submit with: {'qsub' if scheduler == 'sge' else 'sbatch'} htp_submit.sh",
        "",
        "# ── Python batch driver (htp_batch.py) ───────────────────────────────────",
        "import os",
        "import numpy as np",
        "from ase.io import write",
        "from ase.calculators.vasp import Vasp",
        "from server.execution.htp_agent import HTPDataset",
        "",
        f"DB_PATH = '{db_path}'",
        f"BATCH_SIZE = {batch_size}",
        f"ENCUT = {encut}",
        f"KPTS = {kpts}",
        "",
        "dataset = HTPDataset(db_path=DB_PATH)",
        "",
        "# ── Process pending structures in batches ─────────────────────────────────",
        "pending = dataset.get_pending(limit=BATCH_SIZE)",
        "print(f'Processing {len(pending)} pending structures...')",
        "",
        "for row_id, atoms in pending:",
        "    try:",
        "        calc = Vasp(",
        "            istart=0, icharg=2, ibrion=-1, nsw=0, isif=2,",
        "            encut=ENCUT, kpts=KPTS,",
        "            xc='PBE', gamma=True, prec='Accurate',",
        "            ediff=1e-6, ismear=1, sigma=0.2,",
        "            lwave=False, lcharg=False, lreal='Auto',",
        "            nelm=200, ncore=4, algo='Fast',",
        f"           directory=f'vasp_runs/{{row_id}}',",
        "        )",
        "        atoms.set_calculator(calc)",
        "        energy = atoms.get_potential_energy()",
        "        forces = atoms.get_forces()",
        "        try:",
        "            stress = atoms.get_stress()",
        "        except Exception:",
        "            stress = None",
        "        dataset.mark_done(row_id, energy, forces, stress)",
        "        print(f'  Row {row_id}: E={energy:.4f} eV, done.')",
        "    except Exception as e:",
        "        dataset.mark_failed(row_id, str(e))",
        "        print(f'  Row {row_id}: FAILED — {e}')",
        "",
        "# ── Export final training set ─────────────────────────────────────────────",
        "n_exported = dataset.export_extxyz('training.xyz', only_done=True)",
        "print(f'Exported {n_exported} structures to training.xyz')",
        "print(f'Dataset stats: {dataset.stats()}')",
        "",
        "# ── Cluster submission script ─────────────────────────────────────────────",
        f"SUBMIT_SCRIPT = '''{submit_block_sge}'''",
        "with open('htp_submit.sh', 'w') as f:",
        "    f.write(SUBMIT_SCRIPT)",
        "print('Cluster submission script written to htp_submit.sh')",
    ]
    return "\n".join(lines)


