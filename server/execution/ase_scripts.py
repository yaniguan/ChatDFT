# 13. COHP / LOBSTER bonding analysis
# ─────────────────────────────────────────────────────────────────────────────
def script_cohp(
    encut: int = 400,
    kpoints: str = "8 8 1",
    system_desc: str = "",
    atom_pairs: str = "",
    **_kw,
) -> str:
    """COHP bonding analysis: VASP SCF with ISYM=-1, then LOBSTER."""
    kpts = [int(x) for x in kpoints.replace("x"," ").split()[:3]] if kpoints else [8,8,1]
    pair_hint = atom_pairs or "e.g. '1 2' for adsorbate-surface bond"
    lines = [
        _header(f"COHP bonding analysis (VASP + LOBSTER) — {system_desc or 'adsorbate-surface'}"),
        "from ase.io import read",
        "from ase.calculators.vasp import Vasp",
        "import os",
        "",
        "atoms = read('CONTCAR')",
        "",
        "# VASP SCF: ISYM=-1 mandatory — LOBSTER needs all k-points explicitly computed",
        "calc = Vasp(",
        "    istart=0, icharg=2,",
        "    ibrion=-1, nsw=0,",
        f"    encut={encut},",
        f"    kpts={kpts},",
        "    xc='PBE', gamma=True,",
        "    ediff=1e-6,",
        "    ismear=1, sigma=0.2,",
        "    isym=-1,       # *** MANDATORY: break symmetry for LOBSTER ***",
        "    lwave=True,    # LOBSTER reads WAVECAR",
        "    lcharg=True,",
        "    lorbit=11,     # DOSCAR + orbital populations for LOBSTER",
        "    ncore=4,",
        ")",
        "atoms.set_calculator(calc)",
        "atoms.get_potential_energy()",
        "print('VASP SCF done — WAVECAR, CHGCAR, DOSCAR written.')",
        "",
        "# ── Generate LOBSTER mainInput ───────────────────────────────────────────",
        "lobster_input = '''",
        "! LOBSTER mainInput — generated by ChatDFT",
        "! Edit atom pairs to match your adsorbate-surface bond",
        "COHPstartEnergy  -20.0",
        "COHPendEnergy     5.0",
        "usebasisSet       pbeVaspFit2015",
        "! Specify atom pairs for COHP analysis:",
        f"! {pair_hint}",
        "! Example: cohpGenerator from 1 to 5 type Fe type C",
        "cohpGenerator from 1 to 5",
        "saveProjectionToFile",
        "'''.strip()",
        "",
        "with open('lobsterin', 'w') as f:",
        "    f.write(lobster_input)",
        "print('lobsterin written.')",
        "print('Run LOBSTER: lobster   (requires lobster binary: schmeling.ac.at/lobster/)')",
        "print('Output: COHPCAR.lobster — plot with lobsterpy or vesta')",
        "print('Bonding: -ICOHP > 0 = bonding interaction; < 0 = antibonding')",
    ]
    return "\n".join(lines)


