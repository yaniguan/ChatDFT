# server/execution/ase_scripts.py
# -*- coding: utf-8 -*-
"""
ASE calculation script templates.
Each function returns a complete Python script string ready to run with ASE+VASP.

Scripts generated:
  ase-surface.py   — build clean slab (fcc111/bcc110/hcp0001)
  ase-molecule.py  — build gas-phase molecule from PubChem
  ase-geo.py       — geometry optimization (ISIF=2 or 0)
  ase-freq.py      — frequency / ZPE / entropy (IBRION=5)
  ase-neb.py       — CI-NEB transition state search
  ase-gcdft.py     — grand-canonical DFT / VASPsol potential sweep
  ase-dos.py       — DOS / PDOS calculation (non-SCF, ICHARG=11)
  ase-band.py      — band structure (HSE or PBE, non-SCF)
  ase-elf.py       — electron localization function (NCORE=1 required)
  ase-static.py    — static SCF (writes WAVECAR+CHGCAR for follow-ups)
  ase-bader.py     — Bader charge analysis (LAECHG=True)
  ase-cdd.py       — charge density difference (3 static calcs)
  ase-wf.py        — work function (LVHAR=True, LOCPOT parsing)
  ase-cohp.py      — COHP bonding analysis (ISYM=-1, LOBSTER input)
"""
from __future__ import annotations
from typing import Any, Dict

# ─────────────────────────────────────────────────────────────────────────────
# SMILES quick-lookup used in surface/molecule scripts
# ─────────────────────────────────────────────────────────────────────────────
_SMILES: Dict[str, str] = {
    "C4H10": "CCCC", "C4H8": "C=CCC", "C4H8_2": "CC=CC",
    "C3H8": "CCC", "C3H6": "C=CC", "C2H6": "CC", "C2H4": "C=C",
    "CH4": "C", "CO2": "O=C=O", "CO": "[C-]#[O+]",
    "H2O": "O", "NH3": "N", "H2": "[HH]", "N2": "N#N",
}


def _header(title: str) -> str:
    return f"#!/usr/bin/env python3\n# -*- coding: utf-8 -*-\n\"\"\"\n{title}\nGenerated by ChatDFT — edit parameters as needed.\n\"\"\"\n"


# ─────────────────────────────────────────────────────────────────────────────
# 1. Surface builder
# ─────────────────────────────────────────────────────────────────────────────
def script_surface(
    element: str = "Ag",
    surface_type: str = "111",
    nx: int = 4, ny: int = 4, nlayers: int = 3,
    vacuum: float = 10.0,
    fix_bottom: bool = True,
    **_kw,
) -> str:
    build_fn = {
        "111": "fcc111", "100": "fcc100", "110": "fcc110",
        "0001": "hcp0001", "10m10": "hcp10m10",
        "bcc110": "bcc110", "bcc100": "bcc100",
    }.get(surface_type, "fcc111")

    lines = [
        _header(f"Build {element}({surface_type}) {nx}×{ny}×{nlayers} slab, vacuum={vacuum} Å"),
        "from ase.io.vasp import write_vasp",
        f"from ase.build import {build_fn}, add_adsorbate",
        "import ase.constraints",
        "import numpy as np",
        "",
        f"# Build {element}({surface_type}) slab  (size = ({nx},{ny},{nlayers}), vacuum = {vacuum} Å)",
        f"slab = {build_fn}('{element}', size=({nx},{ny},{nlayers}), vacuum={vacuum})",
        "",
    ]
    if fix_bottom:
        lines += [
            "# Fix bottom layer atoms",
            "base_z = np.array([atom.z for atom in slab]).min()",
            "base_layer_index = [atom.index for atom in slab if abs(base_z - atom.z) < 0.1]",
            "constraints = ase.constraints.FixAtoms(indices=base_layer_index)",
            "slab.set_constraint(constraints)",
            "",
        ]
    lines += [
        "# Write POSCAR",
        "write_vasp('POSCAR', slab)",
        f"print(f'Written POSCAR: {{len(slab)}} atoms, {element}({surface_type})-{nx}x{ny}x{nlayers}')",
    ]
    return "\n".join(lines)


# ─────────────────────────────────────────────────────────────────────────────
# 2. Molecule builder (PubChem)
# ─────────────────────────────────────────────────────────────────────────────
def script_molecule(
    formula: str = "C4H10",
    smiles: str = "",
    cell_size: float = 20.0,
    **_kw,
) -> str:
    smiles = smiles or _SMILES.get(formula, formula)
    lines = [
        _header(f"Fetch {formula} from PubChem and write POSCAR"),
        "from ase.io.vasp import write_vasp",
        "from ase.data.pubchem import pubchem_atoms_search",
        "",
        f"# Search PubChem by SMILES: {smiles}",
        f"mol = pubchem_atoms_search(smiles='{smiles}')",
        f"mol.set_cell([{cell_size}, {cell_size}, {cell_size}])",
        "mol.center()",
        "",
        "write_vasp('POSCAR', mol)",
        f"print(f'Written POSCAR: {{mol.get_chemical_formula()}} ({{len(mol)}} atoms)')",
    ]
    return "\n".join(lines)


# ─────────────────────────────────────────────────────────────────────────────
# 3. Geometry optimisation
# ─────────────────────────────────────────────────────────────────────────────
def script_geo(
    kpoints: str = "4 4 1",
    encut: int = 400,
    ediffg: float = -0.02,
    isif: int = 2,
    nsw: int = 200,
    is_molecule: bool = False,
    system_desc: str = "",
    **_kw,
) -> str:
    kpts = [int(x) for x in kpoints.replace("x"," ").split()[:3]] if kpoints else [4,4,1]
    if is_molecule:
        kpts = [1,1,1]
        isif = 0
    lines = [
        _header(f"Geometry optimisation — {system_desc or 'surface slab'}"),
        "from ase.io import read, write",
        "from ase.calculators.vasp import Vasp",
        "",
        "atoms = read('POSCAR')",
        "",
        "calc = Vasp(",
        f"    ibrion=2, nsw={nsw}, isif={isif},",
        f"    ediffg={ediffg}, ediff=1e-5,",
        f"    encut={encut},",
        f"    kpts={kpts},",
        "    xc='PBE', gamma=True,",
        "    lwave=False, lcharg=False,",
        "    ncore=4, algo='Fast',",
        ")",
        "atoms.set_calculator(calc)",
        "atoms.get_potential_energy()   # triggers VASP run",
        "",
        "write('CONTCAR_OPT', atoms, format='vasp')",
        "print(f'Energy: {atoms.get_potential_energy():.4f} eV')",
        "print(f'Max force: {max(((f**2).sum())**0.5 for f in atoms.get_forces()):.4f} eV/Å')",
    ]
    return "\n".join(lines)


# ─────────────────────────────────────────────────────────────────────────────
# 4. Frequency / ZPE
# ─────────────────────────────────────────────────────────────────────────────
def script_freq(
    kpoints: str = "4 4 1",
    encut: int = 400,
    potim: float = 0.015,
    nfree: int = 2,
    temperature: float = 500.0,
    adsorbate: str = "",
    **_kw,
) -> str:
    kpts = [int(x) for x in kpoints.replace("x"," ").split()[:3]] if kpoints else [4,4,1]
    lines = [
        _header(f"Frequency / ZPE calculation — {adsorbate or 'adsorbed species'}"),
        "from ase.io import read",
        "from ase.calculators.vasp import Vasp",
        "from ase.thermochemistry import HarmonicThermo",
        "import numpy as np",
        "",
        "atoms = read('CONTCAR')   # use relaxed geometry from geo opt",
        "",
        "# IBRION=5: finite-difference Hessian (does NOT move atoms, NSW=1)",
        "calc = Vasp(",
        "    ibrion=5, nfree=2, nsw=1, isif=0,",
        f"    potim={potim},",
        "    ediff=1e-7,",
        f"    encut={encut},",
        f"    kpts={kpts},",
        "    xc='PBE', gamma=True,",
        "    lwave=False, lcharg=False,",
        "    ncore=4,",
        ")",
        "atoms.set_calculator(calc)",
        "atoms.get_potential_energy()",
        "",
        "# Extract vibrational frequencies from OUTCAR",
        "from ase.io import read as _read",
        "vib_energies = []",
        "with open('OUTCAR') as fh:",
        "    for line in fh:",
        "        if 'meV' in line and 'f  =' in line:",
        "            parts = line.split()",
        "            ev_idx = parts.index('meV') - 1",
        "            vib_energies.append(float(parts[ev_idx]) * 1e-3)  # meV → eV",
        "        elif 'meV' in line and 'f/i=' in line:",
        "            pass  # imaginary — skip for thermo",
        "",
        "if vib_energies:",
        f"    thermo = HarmonicThermo(vib_energies=vib_energies)",
        f"    T = {temperature}",
        "    ZPE = thermo.get_ZPE_correction()",
        "    S   = thermo.get_entropy(T)",
        "    H   = thermo.get_enthalpy(T)",
        "    G   = thermo.get_helmholtz_energy(T, verbose=True)",
        "    print(f'T={T} K   ZPE={ZPE:.4f} eV   S={S*1e3:.2f} meV/K   G={G:.4f} eV')",
        "    # Save summary",
        "    with open('thermo_summary.txt','w') as out:",
        "        out.write(f'T={T} K\\nZPE={ZPE:.6f} eV\\nS={S:.6e} eV/K\\nG={G:.6f} eV\\n')",
    ]
    return "\n".join(lines)


# ─────────────────────────────────────────────────────────────────────────────
# 5. CI-NEB
# ─────────────────────────────────────────────────────────────────────────────
def script_neb(
    n_images: int = 7,
    kpoints: str = "4 4 1",
    encut: int = 400,
    spring: float = -5.0,
    ediffg: float = -0.05,
    step: str = "",
    **_kw,
) -> str:
    kpts = [int(x) for x in kpoints.replace("x"," ").split()[:3]] if kpoints else [4,4,1]
    lines = [
        _header(f"CI-NEB for: {step or 'elementary step'}"),
        "import os",
        "from ase.io import read, write",
        "from ase.calculators.vasp import Vasp",
        "from ase.neb import NEB",
        "from ase.optimize import BFGS",
        "",
        "# Load initial state (IS) and final state (FS)",
        "initial = read('IS/CONTCAR')  # relaxed initial state",
        "final   = read('FS/CONTCAR')  # relaxed final state",
        "",
        f"n_images = {n_images}",
        "images = [initial.copy() for _ in range(n_images + 2)]",
        "images[-1] = final.copy()",
        "",
        "# Interpolate intermediate images",
        "neb = NEB(images, climb=True, allow_shared_calculator=True)",
        "neb.interpolate()",
        "",
        "# Assign VASP calculators to each image",
        "for i, img in enumerate(images[1:-1]):",
        "    img.calc = Vasp(",
        "        ibrion=3, potim=0, nsw=200, isif=2,",
        "        images=n_images,",
        "        lclimb=True,",
        f"        spring={spring},",
        "        ichain=0, lnebcell=False,",
        f"        ediffg={ediffg}, ediff=1e-5,",
        f"        encut={encut},",
        f"        kpts={kpts},",
        "        xc='PBE', gamma=True,",
        "        lwave=False, lcharg=False,",
        "        ncore=4,",
        f"        directory=f'image_{{i+1:02d}}',",
        "    )",
        "",
        "opt = BFGS(neb, trajectory='neb.traj')",
        "opt.run(fmax=0.05)",
        "",
        "# Print barrier",
        "energies = [img.get_potential_energy() for img in images]",
        "barrier = max(energies) - energies[0]",
        "print(f'Barrier = {barrier:.3f} eV')",
        "print(f'Reaction energy = {energies[-1]-energies[0]:.3f} eV')",
    ]
    return "\n".join(lines)


# ─────────────────────────────────────────────────────────────────────────────
# 6. GC-DFT / VASPsol
# ─────────────────────────────────────────────────────────────────────────────
def script_gcdft(
    kpoints: str = "4 4 1",
    encut: int = 400,
    potential_start: float = -1.5,
    potential_end: float = 0.5,
    nelect_step: float = 0.25,
    eb_k: float = 78.4,
    lambda_d: float = 3.0,
    adsorbate: str = "",
    **_kw,
) -> str:
    kpts = [int(x) for x in kpoints.replace("x"," ").split()[:3]] if kpoints else [4,4,1]
    lines = [
        _header(f"GC-DFT / VASPsol potential sweep — {adsorbate or 'surface'}"),
        "import numpy as np",
        "from ase.io import read, write",
        "from ase.calculators.vasp import Vasp",
        "import json",
        "",
        "atoms = read('CONTCAR')   # relaxed adsorption geometry",
        "# Get reference electron count",
        "ref_nelect = None",
        "with open('OUTCAR') as fh:",
        "    for line in fh:",
        "        if 'NELECT' in line and 'number of electrons' in line:",
        "            ref_nelect = float(line.split()[2]); break",
        "if ref_nelect is None:",
        "    ref_nelect = sum(atoms.get_atomic_numbers())  # fallback",
        "",
        f"U_range = np.arange({potential_start}, {potential_end}+0.01, {nelect_step})",
        "results = []",
        "",
        "for U in U_range:",
        f"    delta_nelect = -U / 4.44  # CHE: U vs RHE → ΔNELECT approx",
        "    nelect = ref_nelect + delta_nelect",
        "    calc = Vasp(",
        "        ibrion=2, nsw=100, isif=2,",
        "        ediffg=-0.02, ediff=1e-6,",
        f"        encut={encut},",
        f"        kpts={kpts},",
        "        xc='PBE', gamma=True,",
        "        lwave=False, lcharg=False,",
        "        ncore=4,",
        "        # VASPsol settings",
        "        lsol=True,",
        f"        eb_k={eb_k},",
        f"        lambda_d_debye={lambda_d},",
        "        nelect=int(round(nelect)),",
        "        directory=f'U_{U:.2f}V'.replace('-','m').replace('.','p'),",
        "    )",
        "    atoms_copy = atoms.copy()",
        "    atoms_copy.calc = calc",
        "    try:",
        "        E = atoms_copy.get_potential_energy()",
        "        results.append({'U_V': float(U), 'E_eV': E, 'nelect': int(round(nelect))})",
        "        print(f'U={U:.2f} V  E={E:.4f} eV  NELECT={nelect:.1f}')",
        "    except Exception as e:",
        "        print(f'U={U:.2f} V  FAILED: {e}')",
        "",
        "with open('gcdft_results.json','w') as f:",
        "    json.dump(results, f, indent=2)",
        "print(f'Done: {len(results)} points saved to gcdft_results.json')",
    ]
    return "\n".join(lines)


