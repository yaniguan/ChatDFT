# 14. Single-point VASP for NNP training
# ─────────────────────────────────────────────────────────────────────────────
def script_singlepoint(
    encut: int = 450,
    kpoints: str = "4 4 1",
    system_desc: str = "",
    **_kw,
) -> str:
    """Single-point VASP for NNP training: tight EDIFF, ISIF=2 for stress tensor."""
    kpts = [int(x) for x in kpoints.replace("x", " ").split()[:3]] if kpoints else [4, 4, 1]
    lines = [
        _header(f"Single-point VASP for NNP training — {system_desc or 'structure'}"),
        "from ase.io import read, write",
        "from ase.calculators.vasp import Vasp",
        "import numpy as np",
        "import os",
        "",
        "# Read structure (POSCAR or any ASE-readable format)",
        "atoms = read('POSCAR')",
        "",
        "calc = Vasp(",
        "    istart=0, icharg=2,",
        "    ibrion=-1, nsw=0,          # static — no relaxation",
        "    isif=2,                     # compute stress tensor",
        f"    encut={encut},              # keep consistent across ALL structures",
        f"    kpts={kpts},",
        "    xc='PBE', gamma=True,",
        "    prec='Accurate',",
        "    ediff=1e-6,                 # tight convergence for NNP labels",
        "    ismear=1, sigma=0.2,",
        "    lwave=False, lcharg=False,  # skip heavy files",
        "    lreal='Auto',",
        "    nelm=200,",
        "    ncore=4, algo='Fast',",
        ")",
        "atoms.set_calculator(calc)",
        "",
        "# Run single-point calculation",
        "energy = atoms.get_potential_energy()",
        "forces = atoms.get_forces()",
        "try:",
        "    stress = atoms.get_stress()   # Voigt notation [xx,yy,zz,yz,xz,xy]",
        "except Exception:",
        "    stress = None",
        "",
        "# Store labels in atoms.info / arrays",
        "atoms.info['energy'] = energy",
        "atoms.arrays['forces'] = forces",
        "if stress is not None:",
        "    atoms.info['stress'] = stress.tolist()",
        "",
        "# Export as extXYZ frame (append mode for batch runs)",
        "write('output.xyz', atoms, format='extxyz', append=True)",
        "",
        "print(f'Energy: {energy:.6f} eV')",
        "print(f'Max force: {np.abs(forces).max():.4f} eV/Å')",
        "if stress is not None:",
        "    print(f'Stress (Voigt): {stress}')",
        "print('Frame appended to output.xyz')",
    ]
    return "\n".join(lines)


