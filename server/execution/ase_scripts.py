# 11. Charge density difference (CDD)
# ─────────────────────────────────────────────────────────────────────────────
def script_cdd(
    encut: int = 400,
    kpoints: str = "8 8 1",
    adsorbate: str = "adsorbate",
    surface: str = "surface",
    **_kw,
) -> str:
    """CDD: 3 static calcs (AB, A, B) → ρ_diff = ρ_AB − ρ_A − ρ_B."""
    kpts = [int(x) for x in kpoints.replace("x"," ").split()[:3]] if kpoints else [8,8,1]
    lines = [
        _header(f"Charge density difference: {adsorbate} on {surface}"),
        "import os, shutil",
        "import numpy as np",
        "from ase.io import read",
        "from ase.io.vasp import read_vasp_chgcar, write_vasp",
        "from ase.calculators.vasp import Vasp",
        "",
        "# ── Step 0: Prepare geometries ──────────────────────────────────────────",
        "# Read the RELAXED combined system (AB)",
        "atoms_AB = read('CONTCAR')  # adsorbate + slab, fully relaxed",
        "",
        "# Split: A = slab only (remove adsorbate atoms), B = adsorbate only",
        "# !! Adjust index slices to match your system !!",
        "n_slab = None   # SET THIS: number of slab atoms",
        "if n_slab is None:",
        "    raise ValueError('Set n_slab = number of slab atoms in atoms_AB')",
        "atoms_A = atoms_AB[:n_slab].copy()   # slab only",
        "atoms_B = atoms_AB[n_slab:].copy()   # adsorbate only",
        "# !! Both A and B MUST use the SAME cell as AB !!",
        "atoms_A.set_cell(atoms_AB.get_cell()); atoms_A.pbc = atoms_AB.pbc",
        "atoms_B.set_cell(atoms_AB.get_cell()); atoms_B.pbc = atoms_AB.pbc",
        "",
        "def _make_calc(directory):",
        "    return Vasp(",
        "        istart=0, icharg=2, ibrion=-1, nsw=0,",
        f"        encut={encut}, kpts={kpts},",
        "        xc='PBE', gamma=True,",
        "        ediff=1e-6, ismear=1, sigma=0.2,",
        "        lwave=False, lcharg=True,  # need CHGCAR for each",
        "        ncore=4, directory=directory,",
        "    )",
        "",
        "# ── Step 1: Three static calculations ───────────────────────────────────",
        "for tag, atms in [('AB', atoms_AB), ('A', atoms_A), ('B', atoms_B)]:",
        "    os.makedirs(tag, exist_ok=True)",
        "    write_vasp(f'{tag}/POSCAR', atms)",
        "    print(f'Running static SCF for {tag} ({len(atms)} atoms) ...')",
        "    atms.set_calculator(_make_calc(tag))",
        "    atms.get_potential_energy()",
        "    print(f'{tag} done.  CHGCAR written to {tag}/CHGCAR')",
        "",
        "# ── Step 2: Compute Δρ = ρ_AB − ρ_A − ρ_B ──────────────────────────────",
        "try:",
        "    from ase.io.vasp import read_vasp_chgcar",
        "    chg_AB = read_vasp_chgcar('AB/CHGCAR')",
        "    chg_A  = read_vasp_chgcar('A/CHGCAR')",
        "    chg_B  = read_vasp_chgcar('B/CHGCAR')",
        "    # Arrays are (nx, ny, nz) charge density grids",
        "    delta_rho = chg_AB['total'] - chg_A['total'] - chg_B['total']",
        "    # Save as numpy array",
        "    np.save('delta_rho.npy', delta_rho)",
        "    print(f'CDD saved to delta_rho.npy  shape={delta_rho.shape}')",
        "    print(f'Δρ range: [{delta_rho.min():.4f}, {delta_rho.max():.4f}] e/Å³')",
        "    print('Visualize: copy AB/CHGCAR, replace data with delta_rho, open in VESTA')",
        "    print('Yellow isosurface = charge accumulation; Blue = charge depletion')",
        "except Exception as ex:",
        "    print(f'CDD post-processing warning: {ex}')",
        "    print('Manual: Δρ = CHGCAR_AB − CHGCAR_A − CHGCAR_B  (vaspkit option 31X)')",
    ]
    return "\n".join(lines)


# ─────────────────────────────────────────────────────────────────────────────
# 12. Work function (LVHAR → LOCPOT)
# ─────────────────────────────────────────────────────────────────────────────
def script_wf(
    encut: int = 400,
    kpoints: str = "8 8 1",
    system_desc: str = "",
    **_kw,
) -> str:
    """Work function via LOCPOT planar average: φ = E_vacuum − E_Fermi."""
    kpts = [int(x) for x in kpoints.replace("x"," ").split()[:3]] if kpoints else [8,8,1]
    lines = [
        _header(f"Work function — LVHAR=True + LOCPOT planar average — {system_desc or 'slab'}"),
        "from ase.io import read",
        "from ase.calculators.vasp import Vasp",
        "import numpy as np",
        "",
        "atoms = read('CONTCAR')",
        "",
        "calc = Vasp(",
        "    istart=0, icharg=2,",
        "    ibrion=-1, nsw=0,",
        f"    encut={encut},",
        f"    kpts={kpts},",
        "    xc='PBE', gamma=True,",
        "    ediff=1e-6,",
        "    ismear=1, sigma=0.2,",
        "    lvhar=True,    # write LOCPOT (total electrostatic potential)",
        "    ldipol=True,   # dipole correction — critical for asymmetric slabs",
        "    idipol=3,      # correct along z-axis (slab normal)",
        "    lwave=False, lcharg=False,",
        "    ncore=4,",
        ")",
        "atoms.set_calculator(calc)",
        "atoms.get_potential_energy()",
        "",
        "# ── Parse LOCPOT: planar average along z ────────────────────────────────",
        "try:",
        "    from ase.io.vasp import read_vasp_out",
        "    from ase.calculators.vasp import VaspChargeDensity",
        "    locpot = VaspChargeDensity('LOCPOT')",
        "    v = locpot.chg[0]          # 3D potential array",
        "    # Planar average: mean over x,y at each z slice",
        "    v_z = v.mean(axis=(0, 1))  # shape: (nz,)",
        "    nz = len(v_z)",
        "    z_coords = np.linspace(0, atoms.get_cell()[2,2], nz, endpoint=False)",
        "    np.savetxt('locpot_avg.dat', np.column_stack([z_coords, v_z]),",
        "               header='z_Angstrom  V_eV')",
        "    # E_vacuum = max of planar-averaged potential in vacuum region",
        "    # E_Fermi from OUTCAR",
        "    ef = calc.get_fermi_level()",
        "    e_vac = v_z.max()",
        "    wf = e_vac - ef",
        "    print(f'E_Fermi   = {ef:.4f} eV')",
        "    print(f'E_vacuum  = {e_vac:.4f} eV')",
        "    print(f'Work function φ = {wf:.4f} eV')",
        "    print('LOCPOT planar average saved to locpot_avg.dat')",
        "except Exception as ex:",
        "    print(f'LOCPOT parse warning: {ex}')",
        "    print('Manual: vaspkit option 426 (work function from LOCPOT)')",
    ]
    return "\n".join(lines)


